void dfs(int i, int j) {
	f[i][j] = 0;
	//printf("%d, %d %d\n", i, j, banbok);
	banbok = banbok + 1;
	save[i][j] = banbok;
	square();
	if (i - 1 > 0 && map[i - 1][j] != 0 && f[i - 1][j] == 1) dfs(i - 1, j); //위
	if (j + 1 < 20 && map[i][j + 1] != 0 && f[i][j + 1] == 1) dfs(i, j + 1); //오른쪽
	if (i + 1 < 20 && map[i + 1][j] != 0 && f[i + 1][j] == 1) dfs(i + 1, j); //아래
	if (j - 1 > 0 && map[i][j - 1] != 0 && f[i][j - 1] == 1) dfs(i, j - 1); //왼쪽
}

